#include "libft.h"

void	ft_putnbr(int n)
{
	long int	ln;

	ln = n;
	if (ln < 0)
	{
		ft_putchar('-');
		ln = -ln;
	}
	if (ln >= 10)
	{
		ft_putnbr(ln / 10);
	}
	ft_putchar('0' + (ln % 10));
}
